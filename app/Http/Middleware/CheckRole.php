<?php

namespace App\Http\Middleware;

use Closure;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;

class CheckRole
{
    public function handle(Request $request, Closure $next, $role)
    {
        if (!Auth::check()) {
            abort(404);
        }

        $user = Auth::user();

        if ($user->role !== $role) {
            abort(404);
        }

        return $next($request);
    }
}
